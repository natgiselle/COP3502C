# Module 4 Lecture: Loops
# Exercise 1: Write a program that prints from 1 to 100

print("First Loop variation counting from 1 to 100:")
count = 1
while count <= 100:
    print(count)
    count += 1

# or you can do this

print("\nSecond Loop variation counting from 1 to 100:")
second_count = 0
while second_count < 100:
    second_count += 1
    print(second_count)
